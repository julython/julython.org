// assetgen runs Tailwind CSS, hashes the output, writes the hashed file to
// web/assets/, and generates internal/components/assets_gen.go with the
// resulting filenames as constants for use in templates.
//
// Usage:
//
//	go run ./cmd/assetgen          # production: minified + hashed
//	go run ./cmd/assetgen -dev     # dev: unminified, fixed filename
//
//go:generate go run ./cmd/assetgen
package main

import (
	"crypto/sha256"
	"flag"
	"fmt"
	"os"
	"os/exec"
	"path/filepath"
)

const (
	tailwindBin  = "bin/tailwindcss"
	inputCSS     = "web/input.css"
	assetsDir    = "web/assets"
	manifestPath = "internal/components/assets_gen.go"
)

func main() {
	dev := flag.Bool("dev", false, "dev mode: no hash, no minify")
	flag.Parse()

	cssName, err := buildCSS(*dev)
	if err != nil {
		fatalf("css build: %v\n", err)
	}

	htmxName, err := findVendored("htmx-*.min.js")
	if err != nil {
		fatalf("htmx: %v\n", err)
	}

	mermaidName, err := findVendored("mermaid-*.min.js")
	if err != nil {
		fatalf("mermaid: %v\n", err)
	}

	if err := writeManifest(cssName, htmxName, mermaidName); err != nil {
		fatalf("manifest: %v\n", err)
	}

	fmt.Printf("assets_gen.go → tailwind: %s  htmx: %s mermaid: %s\n", cssName, htmxName, mermaidName)
}

func buildCSS(dev bool) (string, error) {
	if err := os.MkdirAll("tmp", 0o755); err != nil {
		return "", err
	}
	tmp := "tmp/tailwind-out.css"

	args := []string{"-i", inputCSS, "-o", tmp}
	if !dev {
		args = append(args, "--minify")
	}
	cmd := exec.Command(tailwindBin, args...)
	cmd.Stdout = os.Stdout
	cmd.Stderr = os.Stderr
	if err := cmd.Run(); err != nil {
		return "", fmt.Errorf("tailwindcss: %w", err)
	}

	data, err := os.ReadFile(tmp)
	if err != nil {
		return "", err
	}

	var name string
	if dev {
		name = "tailwind.css"
	} else {
		hash := fmt.Sprintf("%x", sha256.Sum256(data))[:8]
		name = fmt.Sprintf("tailwind.%s.css", hash)

		// Remove stale hashed versions.
		old, _ := filepath.Glob(filepath.Join(assetsDir, "tailwind.*.css"))
		for _, f := range old {
			if filepath.Base(f) != name {
				_ = os.Remove(f)
			}
		}
	}

	dest := filepath.Join(assetsDir, name)
	if err := os.WriteFile(dest, data, 0o644); err != nil {
		return "", err
	}
	return name, nil
}

// findVendored finds a vendored asset matching a glob pattern in web/assets/.
func findVendored(pattern string) (string, error) {
	matches, _ := filepath.Glob(filepath.Join(assetsDir, pattern))
	if len(matches) == 0 {
		return "", fmt.Errorf("%s not found in %s — run: make setup", pattern, assetsDir)
	}
	return filepath.Base(matches[0]), nil
}

func writeManifest(cssName, htmxName, mermaidName string) error {
	content := fmt.Sprintf(`// Code generated by assetgen — DO NOT EDIT.
package components

const (
	AssetTailwindCSS = "/assets/%s"
	AssetHTMX        = "/assets/%s"
	AssetMermaid     = "/assets/%s"
)
`, cssName, htmxName, mermaidName)
	return os.WriteFile(manifestPath, []byte(content), 0o644)
}

func fatalf(format string, args ...any) {
	fmt.Fprintf(os.Stderr, "assetgen: "+format, args...)
	os.Exit(1)
}
