func TestWebLLMIndexAbs(t *testing.T) {
	t.Run("missing", func(t *testing.T) {
		t.Chdir(t.TempDir())
		_, err := webLLMIndexAbs()
		require.Error(t, err)
		require.Contains(t, err.Error(), "missing")
	})

	t.Run("ok", func(t *testing.T) {
		root := t.TempDir()
		t.Chdir(root)
		idx := filepath.Join("web", "vendor", "mlc-web-llm", "lib", "index.js")
		require.NoError(t, os.MkdirAll(filepath.Dir(idx), 0o755))
		require.NoError(t, os.WriteFile(idx, []byte("// stub\n"), 0o644))

		got, err := webLLMIndexAbs()
		require.NoError(t, err)
		require.True(t, filepath.IsAbs(got))
		require.Equal(t, filepath.Join(root, idx), got)
	})
}

func TestWriteHashed(t *testing.T) {
	root := t.TempDir()
	t.Chdir(root)

	data := []byte("hello assetgen")

	t.Run("dev fixed name", func(t *testing.T) {
		name, err := writeHashed(data, "out", ".txt", true)
		require.NoError(t, err)
		require.Equal(t, "out.txt", name)
		b, err := os.ReadFile(filepath.Join("web", "assets", "out.txt"))
		require.NoError(t, err)
		require.Equal(t, data, b)
	})

	t.Run("prod content hash", func(t *testing.T) {
		wantHash := fmt.Sprintf("%x", sha256.Sum256(data))[:8]
		name, err := writeHashed(data, "out", ".txt", false)
		require.NoError(t, err)
		require.Equal(t, "out."+wantHash+".txt", name)
		b, err := os.ReadFile(filepath.Join("web", "assets", name))
		require.NoError(t, err)
		require.Equal(t, data, b)
	})

	t.Run("removes stale hashed sibling", func(t *testing.T) {
		require.NoError(t, os.MkdirAll("web/assets", 0o755))
		stale := filepath.Join("web", "assets", "zap.aaaaaaaa.js")
		require.NoError(t, os.WriteFile(stale, []byte("old"), 0o644))

		_, err := writeHashed([]byte("newcontent"), "zap", ".js", false)
		require.NoError(t, err)
		_, err = os.Stat(stale)
		require.True(t, os.IsNotExist(err), "stale hashed file should be removed")

		matches, err := filepath.Glob(filepath.Join("web", "assets", "zap.*.js"))
		require.NoError(t, err)
		require.Len(t, matches, 1)
	})
}

