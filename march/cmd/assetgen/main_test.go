func TestWriteManifest(t *testing.T) {
	root := t.TempDir()
	t.Chdir(root)
	dir := filepath.Dir(manifestPath)
	require.NoError(t, os.MkdirAll(dir, 0o755))

	err := writeManifest("a.css", "b.js", "c.js", "d.js", "e.js", "f.js")
	require.NoError(t, err)

	b, err := os.ReadFile(manifestPath)
	require.NoError(t, err)
	s := string(b)
	require.True(t, strings.HasPrefix(s, "// Code generated by assetgen"))
	require.Contains(t, s, `AssetTailwindCSS = "/assets/a.css"`)
	require.Contains(t, s, `AssetHTMX        = "/assets/b.js"`)
	require.Contains(t, s, `AssetMermaid     = "/assets/c.js"`)
	require.Contains(t, s, `AssetAnalyzer    = "/assets/d.js"`)
	require.Contains(t, s, `AssetWorker      = "/assets/e.js"`)
	require.Contains(t, s, `AssetLLMWorker   = "/assets/f.js"`)
}

func TestBuildJS_trivialEntry(t *testing.T) {
	t.Chdir(t.TempDir())

	entry := filepath.Join("web", "js", "_assetgen_test_entry.ts")
	require.NoError(t, os.MkdirAll(filepath.Dir(entry), 0o755))
	require.NoError(t, os.WriteFile(entry, []byte("export const x = 1;\n"), 0o644))

	stub := filepath.Join("web", "vendor", "mlc-web-llm", "lib", "index.js")
	require.NoError(t, os.MkdirAll(filepath.Dir(stub), 0o755))
	require.NoError(t, os.WriteFile(stub, []byte("export {};\n"), 0o644))
	alias, err := filepath.Abs(stub)
	require.NoError(t, err)

	name, err := buildJS("web/js/_assetgen_test_entry.ts", "trivial", true, alias)
	require.NoError(t, err)
	require.Equal(t, "trivial.js", name)

	out := filepath.Join("web", "assets", name)
	b, err := os.ReadFile(out)
	require.NoError(t, err)
	require.Contains(t, string(b), "x")
}
